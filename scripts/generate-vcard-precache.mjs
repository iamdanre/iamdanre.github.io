import { createHash } from 'node:crypto'
import { promises as fs } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)
const repoRoot = path.resolve(__dirname, '..')
const vcardDir = path.join(repoRoot, 'vcard')
const swDir = vcardDir
const manifestPath = path.join(vcardDir, 'precache-manifest.js')

const INCLUDED_EXTENSIONS = new Set(['.css', '.html', '.ico', '.js', '.json', '.mjs', '.png', '.svg', '.webp'])
const EXCLUDED_FILES = new Set(['.DS_Store', 'precache-manifest.js', 'sw.js', 'workbox-sw.js'])
const EXTRA_FILES = [path.join(repoRoot, 'img', 'qr.webp')]

const toPosixPath = (value) => value.split(path.sep).join('/')

const toSWRelativeURL = (absolutePath) => {
  const relativePath = path.relative(swDir, absolutePath)
  const normalizedPath = toPosixPath(relativePath)
  return normalizedPath.startsWith('.') ? normalizedPath : `./${normalizedPath}`
}

const hashFile = async (absolutePath) => {
  const content = await fs.readFile(absolutePath)
  return createHash('sha256').update(content).digest('hex').slice(0, 16)
}

const shouldIncludeFile = (entryName) => {
  if (entryName.startsWith('_')) return false
  if (EXCLUDED_FILES.has(entryName)) return false
  return INCLUDED_EXTENSIONS.has(path.extname(entryName).toLowerCase())
}

const collectFiles = async (dirPath) => {
  const dirEntries = await fs.readdir(dirPath, { withFileTypes: true })
  const files = []

  for (const entry of dirEntries) {
    if (entry.name.startsWith('.')) continue

    const absolutePath = path.join(dirPath, entry.name)
    if (entry.isDirectory()) {
      files.push(...(await collectFiles(absolutePath)))
      continue
    }

    if (shouldIncludeFile(entry.name)) files.push(absolutePath)
  }

  return files
}

const uniqueSortedFiles = (files) => Array.from(new Set(files)).sort((left, right) => left.localeCompare(right))

const buildManifestEntries = async () => {
  const discoveredFiles = await collectFiles(vcardDir)
  const files = uniqueSortedFiles([...discoveredFiles, ...EXTRA_FILES])

  const entries = await Promise.all(
    files.map(async (absolutePath) => ({
      url: toSWRelativeURL(absolutePath),
      revision: await hashFile(absolutePath)
    }))
  )

  return entries.sort((left, right) => left.url.localeCompare(right.url))
}

const createManifestSource = async () => {
  const manifestEntries = await buildManifestEntries()
  const manifestSource = [
    '// this file is generated by scripts/generate-vcard-precache.mjs',
        `self.__VCARD_PRECACHE_MANIFEST = ${JSON.stringify(manifestEntries, null, 4)}`,
        ''
  ].join('\n')

  return { manifestEntries, manifestSource }
}

const writeManifest = async () => {
  const { manifestEntries, manifestSource } = await createManifestSource()
  const currentSource = await fs.readFile(manifestPath, 'utf8').catch((error) => {
    if (error.code === 'ENOENT') return null
    throw error
  })

  if (currentSource === manifestSource) {
    console.info(`Precache manifest unchanged (${manifestEntries.length} entries).`)
    return { changed: false, count: manifestEntries.length }
  }

  await fs.writeFile(manifestPath, manifestSource, 'utf8')
  console.info(`Generated ${path.relative(repoRoot, manifestPath)} with ${manifestEntries.length} entries.`)
  return { changed: true, count: manifestEntries.length }
}

writeManifest().catch((error) => {
  console.error('Failed to generate the vcard precache manifest.')
  console.error(error)
  process.exitCode = 1
})
